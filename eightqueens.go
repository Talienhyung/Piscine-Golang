func transformation(n int) []int {
	digits := make([]int, 0)
	t2 := make([]int, 0)
	for n > 0 {
		p := n % 10
		digits = append(digits, p)
		n = n / 10
	}
	for i := len(digits) - 1; i >= 0; i-- {
		t2 = append(t2, digits[i])
	}
	return t2
}

